def make_memory_agent_without_llm() -> MemoryAgent:
    """
    Create a MemoryAgent instance without calling __init__.

    This avoids initializing ChatGoogleGenerativeAI in unit tests,
    so no GOOGLE_API_KEY/GEMINI_API_KEY is required.
    """
    return MemoryAgent.__new__(MemoryAgent)


def make_ticket(
    description: str = "My iPhone battery drains quickly after the latest update.",
) -> Ticket:
    return Ticket(
        ticket_id="ticket-001",
        turn_id="turn-001",
        source="test",
        description=description,
        domain="technical_support",
        subdomain="battery_life",
        product="iphone",
    )


def make_summary(
    problem: str = "The user reports fast battery drain after a recent iPhone update.",
    context: str = "The issue concerns battery life and may require troubleshooting steps.",
    intent: str = "Get help fixing the battery drain issue.",
) -> SummaryOutput:
    return SummaryOutput(
        problem=problem,
        context=context,
        intent=intent,
    )


def make_response(
    response: str = (
        "Please check battery health, update your apps, and reduce background activity. "
        "If the phone keeps overheating, contact support."
    ),
    resolution_type: str = "troubleshooting_steps",
    requires_escalation: bool = False,
    escalation_channel: str = "none",
) -> ResponseOutput:
    return ResponseOutput(
        response=response,
        tone="professional",
        resolution_type=resolution_type,
        requires_escalation=requires_escalation,
        confidence=0.8,
        escalation_channel=escalation_channel,
    )


def make_memory_update_input(
    previous_memory: ConversationMemory | None = None,
    ticket: Ticket | None = None,
    summary: SummaryOutput | None = None,
    response: ResponseOutput | None = None,
) -> MemoryUpdateInput:
    return MemoryUpdateInput(
        ticket=ticket or make_ticket(),
        previous_memory=previous_memory,
        summary=summary or make_summary(),
        response=response or make_response(),
    )


@pytest.fixture
def memory_agent() -> MemoryAgent:
    agent = make_memory_agent_without_llm()
    agent.structured_llm = MagicMock()
    return agent


def extract_message_contents(messages):
    return [message.content for message in messages]


