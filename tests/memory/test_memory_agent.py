@requires_live_llm
def test_memory_agent_live_does_not_confuse_assistant_suggestion_with_user_action():
    agent = MemoryAgent()

    ticket = make_ticket(
        description="My iPhone battery drains quickly after the latest update."
    )
    summary = make_summary(
        problem="The user reports iPhone battery drain after the latest update.",
        context="The issue concerns battery life after a software update.",
        intent="Get help fixing the battery drain.",
    )
    response = make_response(
        response="Please restart the phone and check battery health.",
        resolution_type="troubleshooting_steps",
    )

    result = agent.update_memory(
        make_memory_update_input(
            previous_memory=None,
            ticket=ticket,
            summary=summary,
            response=response,
        )
    )

    memory_lower = result.memory.lower()

    assert "restart" in memory_lower or "battery health" in memory_lower

    forbidden_phrases = [
        "the user restarted",
        "user restarted",
        "the user has restarted",
        "the user checked battery health",
        "user checked battery health",
        "the user has checked battery health",
    ]

    assert not any(phrase in memory_lower for phrase in forbidden_phrases)


